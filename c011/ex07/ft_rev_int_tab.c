void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	q;
	int	s;

	i = 0;
	q = size - 1;
	while (i < q)
	{
		s = tab[i];
		tab[i] = tab[q];
		tab[q] = s;
		i++;
		q--;
	}
}
